void ShaderProgramDatabase::initRotateProg(bool has_bindless, DeviceContext &dc)
{
  InputLayout il;
  auto ili = shaderDesc.layouts.add(dc, {il});

  ShaderID vs, fs90, fs180, fs270;
  {
    // configure header for vertex index only VS
    ShaderModuleHeader smh = {};
    spirv::ShaderHeader &spvHeader = smh.header;
    spvHeader.descriptorCounts[0].descriptorCount = 0;
    spvHeader.maxConstantCount = 0;
    spvHeader.bonesConstantsUsed = -1; // @TODO: remove
    spvHeader.bRegisterUseMask = 0;
    spvHeader.constBufferCount = 0;
    spvHeader.descriptorCountsCount = 0;
    spvHeader.registerCount = 0;

    smh.hash = spirv::HashValue::calculate(&spvHeader, 1);
    smh.stage = VK_SHADER_STAGE_VERTEX_BIT;

    ShaderModuleBlob smb = {};
    // needs to typecast or insert will extend each element from u8 to u32
    const uint8_t *vertDump = rotate_vert_prog;
    size_t vertDumpSz = sizeof(rotate_vert_prog);
    auto from = reinterpret_cast<const uint32_t *>(vertDump);
    auto to = reinterpret_cast<const uint32_t *>(vertDump + vertDumpSz);
    smb.blob.insert(end(smb.blob), from, to);
    smb.hash = spirv::HashValue::calculate(smb.blob.data(), smb.blob.size());

    vs = newShader(dc, smh, smb);

#if VULKAN_LOAD_SHADER_EXTENDED_DEBUG_DATA
    ShaderDebugInfo dbgInf;
    dbgInf.name = "rotateVS"; // -V691
    dbgInf.debugName = "rotateVS";
    attachDebugInfo(vs, dbgInf);
#endif
  }

  {
    // configure header for
    // - 2d tex in t0
    // - output in color 0
    ShaderModuleHeader smh = {};
    spirv::ShaderHeader &spvHeader = smh.header;
    spvHeader.descriptorTypes[1] = VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER;
    spvHeader.imageViewTypes[0] = VK_IMAGE_VIEW_TYPE_2D;
    spvHeader.descriptorCounts[1].type = VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER;
    spvHeader.descriptorCounts[1].descriptorCount = 1;
    spvHeader.tRegisterUseMask = 1;
    spvHeader.outputMask = 1ul << 0;
    spvHeader.registerIndex[1] = spirv::T_SAMPLED_IMAGE_OFFSET + 0;
    spvHeader.imageCheckIndices[0] = 0;
    spvHeader.missingTableIndex[1] = spirv::MISSING_SAMPLED_IMAGE_2D_INDEX;
    spvHeader.imageCount = 1;

    spvHeader.descriptorTypes[0] = VK_DESCRIPTOR_TYPE_UNIFORM_BUFFER_DYNAMIC;
    spvHeader.descriptorCounts[0].type = VK_DESCRIPTOR_TYPE_UNIFORM_BUFFER_DYNAMIC;
    spvHeader.descriptorCounts[0].descriptorCount = 1;
    spvHeader.maxConstantCount = 4;
    spvHeader.bRegisterUseMask = 1;
    spvHeader.registerIndex[0] = spirv::B_CONST_BUFFER_OFFSET + 0;
    spvHeader.constBufferCheckIndices[0] = 0;
    spvHeader.missingTableIndex[0] = spirv::FALLBACK_TO_C_GLOBAL_BUFFER;
    spvHeader.constBufferCount = 1;

    spvHeader.descriptorCountsCount = 2;
    spvHeader.registerCount = 2;

    smh.hash = spirv::HashValue::calculate(&spvHeader, 1);
    smh.stage = VK_SHADER_STAGE_FRAGMENT_BIT;

    auto makeFS = [&](const uint8_t *fragDump, size_t fragDumpSz, const char *dbgName) {
      ShaderModuleBlob smb = {};
      // needs to typecast or insert will extend each element from u8 to u32
      auto from = reinterpret_cast<const uint32_t *>(fragDump);
      auto to = reinterpret_cast<const uint32_t *>(fragDump + fragDumpSz);
      smb.blob.insert(end(smb.blob), from, to);
      smb.hash = spirv::HashValue::calculate(smb.blob.data(), smb.blob.size());

      ShaderID ret = newShader(dc, smh, smb);
#if VULKAN_LOAD_SHADER_EXTENDED_DEBUG_DATA
      ShaderDebugInfo dbgInf;
      dbgInf.name = dbgName; // -V691
      dbgInf.debugName = dbgName;
      attachDebugInfo(ret, dbgInf);
#else
      G_UNUSED(dbgName);
#endif
      return ret;
    };

    {
      const uint8_t *fragDump = has_bindless ? rotate_frag_program_90_bindless : rotate_frag_program_90;
      size_t fragDumpSz = has_bindless ? sizeof(rotate_frag_program_90_bindless) : sizeof(rotate_frag_program_90);
      fs90 = makeFS(fragDump, fragDumpSz, "rotateFS90");
    }

    {
      const uint8_t *fragDump = has_bindless ? rotate_frag_program_180_bindless : rotate_frag_program_180;
      size_t fragDumpSz = has_bindless ? sizeof(rotate_frag_program_180_bindless) : sizeof(rotate_frag_program_180);
      fs180 = makeFS(fragDump, fragDumpSz, "rotateFS180");
    }

    {
      const uint8_t *fragDump = has_bindless ? rotate_frag_program_270_bindless : rotate_frag_program_270;
      size_t fragDumpSz = has_bindless ? sizeof(rotate_frag_program_270_bindless) : sizeof(rotate_frag_program_270);
      fs270 = makeFS(fragDump, fragDumpSz, "rotateFS270");
    }
  }

  rotate90ProgId = progs.graphics.add(dc, {ili, shaders.get(vs), shaders.get(fs90)});
  rotate180ProgId = progs.graphics.add(dc, {ili, shaders.get(vs), shaders.get(fs180)});
  rotate270ProgId = progs.graphics.add(dc, {ili, shaders.get(vs), shaders.get(fs270)});
}
